    //tornar funcao horizontal apenas e criar a vertical
    public boolean isDirectionValid(int x, int y, int zBarco, String direction) {
        if(direction == "Horizontal") {
            return x >= 0 && (x + zBarco - 1) < matriz.length && y >= 0 && y < matriz[0].length;
        }
        else if(direction == "Vertical") {
            return y >= 0 && (y + zBarco - 1) < matriz[0].length && x >= 0;     
        }
        return false;
    }
    
    public void setBarco(JButton actualButton, int zBarco, int x, int y, String direction) {
        int actualX = jPanel1.getComponentZOrder(actualButton);
        int nextX = actualX;
        
        if(direction == "Horizontal") {
            for(int i = x ; i < x + zBarco ; i++) {
                this.matriz[y][i] = zBarco;
            }

            actualButton.setBackground(colorMap.getColor(zBarco));
            
            for(int i = actualX ; i < (actualX + zBarco - 1) ; i++) {
                JButton nextButton = (JButton) jPanel1.getComponent(nextX + 1);
                nextX++;
                nextButton.setBackground(colorMap.getColor(zBarco));
            }
        }
        else if(direction == "Vertical") {
            for(int i = y; i < y + zBarco; i++) {
                this.matriz[i][x] = zBarco;
            }
            
            actualButton.setBackground(colorMap.getColor(zBarco));
            
            for(int i = actualX ; i < (actualX + zBarco - 1) ; i++) {
                JButton nextButton = (JButton) jPanel1.getComponent(nextX + 10);
                nextX = nextX + 10;
                nextButton.setBackground(colorMap.getColor(zBarco));
            }
        }
    }
    
    public boolean validateDirectionColision(int x, int y, int zBarco, String direction) {
        if(direction == "Horizontal") { 
            for(int i = x ; i < x + zBarco ; i++) {
                if(matriz[y][i] > 0) return false;
            }
            return true;
        }
        else if(direction == "Vertical") {
            for (int i = y; i < y + zBarco; i++) {
                if(matriz[i][x] > 0) return false;
            }
            return true;
        }
        return false;
    }
    
    private boolean validatePosition(int x, int y, int zBarco, String direction) {
        if(isDirectionValid(x, y, zBarco, direction) && validateDirectionColision(x, y, zBarco, direction)){
            return true;
        } 
        return false;
    }
    
