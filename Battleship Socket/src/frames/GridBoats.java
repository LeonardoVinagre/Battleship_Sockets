    private void myInitComponents() {
        jPanel1.setLayout(new GridLayout(10,10,0,0));
        for(int i = 0 ; i < 10 ; i++) {
            for(int j = 0 ; j < 10 ; j++) {   
                JButton button = new JButton();
                Dimension buttonSize = new Dimension(30,30); 
                button.setPreferredSize(buttonSize);
                button.setBorder(BorderFactory.createLineBorder(Color.white, 1));
                button.setBackground(Color.LIGHT_GRAY);
                button.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        JButton clickedButton = (JButton) e.getSource();
                        String direction = jComboBox1.getSelectedItem().toString();
                        int x,y;
                        int column = jPanel1.getComponentZOrder(clickedButton);

                        if(column >= 0 && column <= 9) {
                            x = 0;
                            y = column;
                        }else {
                            x = column / 10;
                            y = column % 10;
                        }
                        
                        switch (direction) {
                            case "Horizontal":  
                                if (!listaBarcos.isEmpty() && validatePosition(y, x, listaBarcos.get(0), direction)) {
                                    setBarco(clickedButton, listaBarcos.get(0), y, x, direction);
                                    listaBarcos.remove(0);
                                } 
                            break;
                            
                            case "Vertical":
                                if (!listaBarcos.isEmpty() && validatePosition(y, x, listaBarcos.get(0), direction)) {
                                    setBarco(clickedButton, listaBarcos.get(0), y, x, direction);
                                    listaBarcos.remove(0);
                                }
                                break;
                        }
                    }
                });
                jPanel1.add(button);
            }
        }
        
    }
    
    

