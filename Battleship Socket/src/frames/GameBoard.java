    public void blockTurn() {
        for(int i = 0 ; i < 10 ; i++) {
            for(int j = 0 ; j < 10 ; j++) {
                enemyBoard[i][j].setEnabled(false);
            }
        }
    }
    
    public void unblockTurn() {
        for(int i = 0 ; i < 10 ; i++) {
            for(int j = 0 ; j < 10 ; j++) {
                if(!enabledButtonsBeforeTurn.contains(enemyBoard[i][j])) {
                    enemyBoard[i][j].setEnabled(true);
                } 
            }
        }
    }
    
