    public void showResult(String logMessage) {
        String[] opcoes = {"Confirm"};
        int escolha = JOptionPane.showOptionDialog(this, logMessage, "End of the game",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opcoes, opcoes[0]);

        if(escolha == 0  || escolha == -1) {
            this.frameController.newGame();
            this.dispose();
        }
    }
    
    public void exitGame() {
        ClientFrame newGame = new ClientFrame();
        newGame.setVisible(true);
        try {
            this.frameController.getConnection().sendDisconnectMessage();
        } catch (Throwable ex) {
            Logger.getLogger(GameBoard.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }
    
