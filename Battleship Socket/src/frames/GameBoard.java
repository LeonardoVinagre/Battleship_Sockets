    public void setMyBoard(JPanel panel) {
        panel.setLayout(new GridLayout(10,10,0,0));
        for(int i = 0 ; i < 10 ; i++) {
            for(int j = 0 ; j < 10 ; j++) {   
                JLabel label = new JLabel();
                myBoard[i][j] = label;
                Dimension buttonSize = new Dimension(30,30); 
                label.setPreferredSize(buttonSize);
                label.setBorder(BorderFactory.createLineBorder(Color.white, 1));
                label.setBackground(Color.LIGHT_GRAY);
                label.setOpaque(true);
                panel.add(label);
            }
        }
    }
    
    public void setEnemyBoard(JPanel panel, boolean firstPlay) {
        panel.setLayout(new GridLayout(10,10,0,0));
        for(int i = 0 ; i < 10 ; i++) {
            for(int j = 0 ; j < 10 ; j++) {   
                JButton button = new JButton();
                button.setEnabled(firstPlay);
                enemyBoard[i][j] = button;
                Dimension buttonSize = new Dimension(30,30); 
                button.setPreferredSize(buttonSize);
                button.setBorder(BorderFactory.createLineBorder(Color.white, 1));
                button.setBackground(Color.LIGHT_GRAY);
                
                button.addActionListener(new ActionListener() {
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        enabledButtonsBeforeTurn.add(button);
                        JButton clickedButton = (JButton) e.getSource();
                        int zOrder = panel.getComponentZOrder(clickedButton);
                        frameController.getConnection().createBombOrder(zOrder);
                    }
                });
                
                panel.add(button);
            }
        }
    }
    
    public void fillMyBoard() {
        int[][] myBoats = frameController.getConnection().getClientSideData().getMatriz();
        for(int i = 0 ; i < 10 ; i++) {
            for(int j = 0 ; j < 10 ; j++) {
                if(myBoats[i][j] > 0) {
                    myBoard[i][j].setBackground(numberToColorMap.getColor(myBoats[i][j]));
                }
            }
        }
    }
    
    public void addLogMessage(String message) {
        SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss");
        String logMessage = sdf.format(new Date()) + ": " + message + "\n";
        jTextArea1.append(logMessage);
        jTextArea1.setCaretPosition(jTextArea1.getDocument().getLength());
    }
    
    public void setMissOnEnemy(int x, int y) {
        JLabel iconLabel = new JLabel(new ImageIcon("src/commons/img/sea.png"));
        iconLabel.setLayout(new FlowLayout(FlowLayout.CENTER));
        enemyBoard[x][y].setBorderPainted(false);
        enemyBoard[x][y].setContentAreaFilled(false);
        enemyBoard[x][y].setFocusPainted(false);
        enemyBoard[x][y].setOpaque(false);
        enemyBoard[x][y].setEnabled(false);
        enemyBoard[x][y].setLayout(new BorderLayout());
        enemyBoard[x][y].add(iconLabel);
    }
    
